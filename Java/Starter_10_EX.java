public class Starter_10_EX {
    public static void main(String[] args) {
        // Example of using char and boolean types in Java
        char letterA = 'A'; // Character type
        char unicodeChar = '\u03A9'; // Unicode character (Omega symbol)
        boolean isJavaFun = true; // Boolean type
        boolean isFishTasty = false; // Another boolean example

        // Output the values
        System.out.println("Character A: " + letterA);
        System.out.println("Unicode Character (Omega): " + unicodeChar);
        System.out.println("Is Java Fun? " + isJavaFun);
        System.out.println("Is Fish Tasty? " + isFishTasty);
    }
}